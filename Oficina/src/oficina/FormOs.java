    private void calcularTotal()
    {
        DefaultListModel d = (DefaultListModel) lServicos.getModel();
        valor = 0;
        
        for(int i = 0; i < d.getSize(); i++)
        {
            valor += Double.parseDouble(((Servico) d.elementAt(i)).getPreco());
        }
    }
    
