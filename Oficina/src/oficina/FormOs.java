    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btCadastrarActionPerformed
    {//GEN-HEADEREND:event_btCadastrarActionPerformed
        if(tfKmEntrada.getText().equals("") || tfDataInicio.getText().equals("") 
                || tfHoraInicio.getText().equals("  :  ")
                || placa == null) // || clienteCpfCnpj == null
        {
            lbAlerta.setText("Por favor, preencha os campos destacados em vermelho.");
            
            if(tfKmEntrada.getText().equals(""))
                lbKmEntrada.setForeground(Color.red);
            else
                lbKmEntrada.setForeground(Color.black);

            if(tfDataInicio.getText().equals(""))
                lbDataInicio.setForeground(Color.red);
            else
                lbDataInicio.setForeground(Color.black);

            if(tfHoraInicio.getText().equals("  :  "))
                lbHoraInicio.setForeground(Color.red);
            else
                lbHoraInicio.setForeground(Color.black);
            
            if(placa == null)
                lbCarroText.setForeground(Color.red);
            else
                lbCarroText.setForeground(Color.black);
            
            //if cliente não informado etc
        }
        
        else
        {
            lbAlerta.setText(null);
            lbKmEntrada.setForeground(Color.black);
            lbDataInicio.setForeground(Color.black);
            lbHoraInicio.setForeground(Color.black);
            lbCarroText.setForeground(Color.black);
            lbClienteText.setForeground(Color.black);
            
            os = new OrdemDeServico();

            os.setCodigoOs(0);
            os.setPlacaCarro(placa);
            os.setCodCliente(clienteCpfCnpj);
            os.setKmEntrada(tfKmEntrada.getText());
            os.setKmSaida(tfKmSaida.getText());
            os.setDataInicio(tfDataInicio.getText());
            os.setDataFim(tfDataFim.getText());
            os.setHoraInicio(tfHoraInicio.getText());
            os.setHoraFim(tfHoraFim.getText());
            os.setTipo(cboxTipo.getSelectedItem().toString());
            os.setEstado(cboxEstado.getSelectedItem().toString());

            DefaultListModel d = (DefaultListModel) lServicos.getModel();

            while(!d.isEmpty())
            {
                os.servicos.add((Servico) d.remove(0));
            };
            
            tfKmEntrada.setText("");
            tfDataInicio.setText("");
            tfHoraInicio.setText("");
            lbCarroText.setText("NÃO INFORMADO");
            placa = null;
            lbClienteText.setText("NÃO INFORMADO");
            clienteCpfCnpj = null;
            cboxTipo.setSelectedIndex(0);
            
            JOptionPane.showMessageDialog(null, "Ordem de serviço cadastrada com sucesso!");
        }
    }//GEN-LAST:event_btCadastrarActionPerformed

